function add(a, b){
  var result = a + b
  return result
}

function subtract(a, b){
  var result = a - b
  return result
}

function multiply(a, b){
  var result = a * b
  return result
}

function divide(a, b){
  var result = a / b
  return result
}

function inc(n){
  n++
  return n
}

function dec(n){
  n--
  return n
}

function makeInt(n){
  parseInt(n, 10)
  return n
}

function preserveDecimal(n){
  parseFloat(n)
  return n
}